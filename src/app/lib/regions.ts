const LOOKUP = (() => {
  const m = new Map<string, RegionKey>();
  (Object.keys(REGION_PROVINCES) as RegionKey[]).forEach((region) => {
    REGION_PROVINCES[region].forEach((name) => m.set(name, region));
  });
  return m;
})();

export function getRegionByProvinceNameThai(name?: string): RegionKey {
  const key = (name ?? "").trim();
  return (LOOKUP.get(key) ?? "central") as RegionKey; // fallback
}
